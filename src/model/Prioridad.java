package model;

/**
 * Enumeracion que representa los niveles de prioridad de una tarea.
 * Los valores posibles son: ALTA, MEDIA, BAJA.
 * 
 * @author ISC Israel de Jesus Mar Parada
 * @version 1.0
 */
public enum Prioridad {
    
    /** Prioridad alta - tareas urgentes y criticas. */
    ALTA,
    
    /** Prioridad media - tareas importantes pero no urgentes. */
    MEDIA,
    
    /** Prioridad baja - tareas que pueden esperar. */
    BAJA
    
}//fin de la clase